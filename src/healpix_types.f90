MODULE healpix_types
!---------------------
  !...This module sets the types used in the Fortran 90 modules
  !...of the HEALPIX distribution and follows the example of Numerical Recipes
  !...
  !...Benjamin D. Wandelt October 1997
  !...Eric Hivon June 1998
  !...
  INTEGER, PARAMETER :: i4b = SELECTED_INT_KIND(9)
  INTEGER, PARAMETER :: sp  = KIND(1.0E0)
  INTEGER, PARAMETER :: dp  = KIND(1.0D0)
  INTEGER, PARAMETER :: lgt = KIND(.TRUE.)
  INTEGER, PARAMETER :: spc = KIND((1.0,  1.0))
  INTEGER, PARAMETER :: dpc = KIND((1.0d0,1.0d0))
  !
  INTEGER,  PARAMETER:: max_i4b = HUGE(1_i4b)
  REAL(SP), PARAMETER:: max_sp  = HUGE(1.0_sp)
  REAL(DP), PARAMETER:: max_dp  = HUGE(1.0_dp)


  ! Numerical Constant (Double precision)
  REAL(DP), PARAMETER :: QUARTPI=0.785398163397448309615660845819875721049_dp
  REAL(DP), PARAMETER :: HALFPI= 1.570796326794896619231321691639751442099_dp
  REAL(DP), PARAMETER :: PI    = 3.141592653589793238462643383279502884197_dp
  REAL(DP), PARAMETER :: TWOPI = 6.283185307179586476925286766559005768394_dp
  REAL(DP), PARAMETER :: FOURPI=12.56637061435917295385057353311801153679_dp
  REAL(DP), PARAMETER :: SQRT2 = 1.41421356237309504880168872420969807856967_dp
  REAL(DP), PARAMETER :: EULER = 0.5772156649015328606065120900824024310422_dp
  REAL(DP), PARAMETER :: SQ4PI_INV = 0.2820947917738781434740397257803862929220_dp
  REAL(DP), PARAMETER :: TWOTHIRD = 0.6666666666666666666666666666666666666666_dp
  !COMPLEX(SPC), PARAMETER :: uimag = CMPLX(0.,1.)


END MODULE healpix_types
